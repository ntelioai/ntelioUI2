    /**
     * Get an icon as an SVG string.
     *
     * @param {string} name  - Icon name (e.g. 'dashboard', 'users', 'settings')
     * @param {number} [size=20] - Width and height in pixels
     * @returns {string} SVG markup string, or empty string if icon not found
     *
     * @example
     * Icons.get('home')        // 20×20 SVG string
     * Icons.get('home', 24)    // 24×24 SVG string
     */
    static get(name, size = 20) {
        const inner = Icons._icons[name]
        if (!inner) return ''
        return `<svg xmlns="http://www.w3.org/2000/svg" width="${size}" height="${size}" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round">${inner}</svg>`
    }

    /**
     * Register a custom icon.
     *
     * @param {string} name  - Icon name
     * @param {string} svg   - SVG inner content (paths, rects, circles — no outer `<svg>` tag)
     *
     * @example
     * Icons.register('rocket', '<path d="M..."/>')
     * Icons.get('rocket')  // → full SVG string
     */
    static register(name, svg) {
        Icons._icons[name] = svg
    }

    /**
     * Register multiple icons at once.
     *
     * @param {Object<string, string>} map - `{ name: svgInnerContent }` pairs
     *
     * @example
     * Icons.registerAll({
     *     rocket: '<path d="M..."/>',
     *     planet: '<circle cx="12" cy="12" r="10"/>'
     * })
     */
    static registerAll(map) {
        Object.assign(Icons._icons, map)
    }

    /**
     * Check if an icon is registered.
     *
     * @param {string} name - Icon name
     * @returns {boolean}
     */
    static has(name) {
        return name in Icons._icons
    }

    /**
     * List all registered icon names.
     *
     * @returns {string[]}
     */
    static names() {
        return Object.keys(Icons._icons)
    }
